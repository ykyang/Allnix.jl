# - General Collections
using Base.Test
@testset "General Collections" begin
c::Vector{Int64} = []
# --------------------------------------------- #
@test Base.isempty(c) == true
c = 5:-1:1
@test Base.isempty(c) == false
# --------------------------------------------- #
c = 1:5
c = Base.empty!(c)
@test Base.isempty(c) == true
# --------------------------------------------- #
c = 10:-1:1
@test Base.length(c) == 10
@test Base.endof(c) == 10
end

